public class Mathematics {

    public int add(int num1,int num2){
        return num1 + num2;
    }

    public int subtraction(int num1, int num2){
        return num1 * num2;
    }

    public double division(double num1, double num2){
        return num1 / num2;
    }

    public int multiplication(int num1, int num2){
        return num1 * num2;
    }

}
